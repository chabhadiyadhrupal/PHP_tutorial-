<?php
// Initialize the variable
$x = 10;
echo "Initial value of x: $x\n";

// Addition Assignment
$x += 5; // $x = $x + 5;
echo "After x += 5: $x\n";

// Subtraction Assignment
$x -= 3; // $x = $x - 3;
echo "After x -= 3: $x\n";

// Multiplication Assignment
$x *= 2; // $x = $x * 2;
echo "After x *= 2: $x\n";

// Division Assignment
$x /= 4; // $x = $x / 4;
echo "After x /= 4: $x\n";

// Modulus Assignment
$x %= 3; // $x = $x % 3;
echo "After x %= 3: $x\n";

// Exponentiation Assignment
$x = 2; // Reset $x
$x **= 3; // $x = $x ** 3;
echo "After x **= 3: $x\n";
?>
